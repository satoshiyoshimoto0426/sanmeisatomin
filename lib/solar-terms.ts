/**
 * 指定年の12節気を時系列で取得
 */
function getSolarTermsForYear(year: number): SolarTerm[] {
  const precise = PRECISE_TABLE[year];
  return SEKKI_NAMES.map((name, i) => ({
    name,
    monthBranch: SEKKI_BRANCHES[i],
    dateTime: precise ? new Date(precise[i]) : approxSekkiDate(year, i),
  }));
}

/**
 * 指定された日付の「節月暦における年」を返す
 *
 * 立春前は前年として扱う。
 * 例: 2024-02-03 → 2023年扱い、2024-02-05 → 2024年扱い
 */
export function getSetsugetsuYear(date: Date): number {
  const calendarYear = date.getFullYear();
  const terms = getSolarTermsForYear(calendarYear);
  const risshun = terms[0].dateTime; // 立春
  return date < risshun ? calendarYear - 1 : calendarYear;
}

/**
 * 指定された日付の「節月暦における月支」を返す
 *
 * 例: 2024-02-03 → 丑（立春前なので前月扱い）
 *     2024-02-05 → 寅（立春後）
 *     2024-03-04 → 寅（啓蟄前）
 *     2024-03-06 → 卯（啓蟄後）
 */
export function getSetsugetsuBranch(date: Date): string {
  const calendarYear = date.getFullYear();

  // 当年の節気を取得
  const currentTerms = getSolarTermsForYear(calendarYear);

  // 立春前なら前年の節気テーブルから探索
  if (date < currentTerms[0].dateTime) {
    const prevTerms = getSolarTermsForYear(calendarYear - 1);
    // 前年の最後の節気（小寒=丑月）から逆順に探索
    for (let i = prevTerms.length - 1; i >= 0; i--) {
      if (date >= prevTerms[i].dateTime) {
        return prevTerms[i].monthBranch;
      }
    }
    // それより前なら前々年の小寒以降
    return '丑'; // フォールバック
  }

  // 立春以降は当年テーブルから探索
  let branch = '寅'; // デフォルト（立春直後）
  for (const term of currentTerms) {
    if (date >= term.dateTime) {
      branch = term.monthBranch;
    } else {
      break;
    }
  }
  return branch;
}

/**
 * 指定された日付の節月暦における (年, 月支) を一括取得
 */
export function getSetsugetsu(date: Date): { year: number; monthBranch: string } {
  return {
    year: getSetsugetsuYear(date),
    monthBranch: getSetsugetsuBranch(date),
  };
}
