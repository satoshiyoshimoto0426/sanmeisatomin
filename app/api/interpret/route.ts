   - 必ず「たとえば〜」「具体的には〜」で始まる具体的場面を2〜3箇所含める
   - 職場のシーン（会議・転職・起業・昇進・ミス等）、人間関係（恋愛・友人・家族）、お金の扱い方、など実生活の場面で語る
